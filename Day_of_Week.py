usr_input=int(input("Input the Day Number:\n"))
if usr_input==1:
    print("Monday")
elif usr_input==2:
    print("Tuesday")
elif usr_input==4:
    print("Thursday")
elif usr_input==5:
    print("Friday")
elif usr_input==6:
    print("Saturday")
elif usr_input==7:
    print("Sunday")
else:
    print("Input Valid Day")
    